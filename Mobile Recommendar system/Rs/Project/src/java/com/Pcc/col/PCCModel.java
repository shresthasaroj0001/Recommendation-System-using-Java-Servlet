package com.Pcc.col;

/**
 *
 * @author Saroj
 */
public class PCCModel {
    private int userID;
    private double pcc;
    private double Wau;

    /**
     * @return the userID
     */
    public int getUserID() {
        return userID;
    }

    /**
     * @param userID the userID to set
     */
    public void setUserID(int userID) {
        this.userID = userID;
    }

    /**
     * @return the pcc
     */
    public double getPcc() {
        return pcc;
    }

    /**
     * @param pcc the pcc to set
     */
    public void setPcc(double pcc) {
        this.pcc = pcc;
    }

    /**
     * @return the Wau
     */
    public double getWau() {
        return Wau;
    }

    /**
     * @param Wau the Wau to set
     */
    public void setWau(double Wau) {
        this.Wau = Wau;
    }
}
